#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	while (s && *s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (s && *s == '\0' && c == '\0')
		return ((char *)s);
	return (NULL);
}
